class Enemy {

    constructor(x, y, no ,word, mean) {
        this.x = x;
        this.y = y;
        this.no = no;
        this.word = word;
        this.mean = mean;
    }
    getX(){
    	return this.x;
    }
    getY(){
        return this.y;
    } 
    getNo(){
        return this.no;
    }
    getWord(){
        return this.word;
    } 
    getMean(){
        return this.mean;
    } 
}
 